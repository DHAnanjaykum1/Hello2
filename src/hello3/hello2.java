    private void tabMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabMouseClicked
        DefaultTableModel model = (DefaultTableModel) tab.getModel();
        int sr = tab.getSelectedRow();
        try {

            TextStudentName.setText(model.getValueAt(sr, 1).toString());
            TextFatherName.setText(model.getValueAt(sr, 2).toString());
            TextMotherName.setText(model.getValueAt(sr, 3).toString());
            String gen = model.getValueAt(sr, 4).toString();
            gender = gen;
            switch (gen) {
                case "Male":
                    TextMale.setSelected(true);
                    break;
                case "Female":
                    TextFimale.setSelected(true);
                    break;
                case "TextOther":
                    TextMale.setSelected(true);
                    break;
                default:
                    break;
            }
            Date d = new Date();
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            d = sdf.parse(model.getValueAt(sr, 5).toString());
            TextDate.setDate(d);
            TextEmail.setText(model.getValueAt(sr, 6).toString());
            TextDepartment.setSelectedItem(model.getValueAt(sr, 7).toString());
            TextMobileNo.setText(model.getValueAt(sr, 8).toString());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_tabMouseClicked

