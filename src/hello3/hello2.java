    public void showData() {
        try {
            con = DriverManager.getConnection(host, username, password);
            pst = con.prepareStatement("select * from studentReg");
            rs = pst.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            q = rsmd.getColumnCount();
            DefaultTableModel model = (DefaultTableModel) tab.getModel();
            model.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                for (i = 1; i <= q; i++) {
                    v.add(rs.getShort("id"));
                    v.add(rs.getString("studentName"));
                    v.add(rs.getString("fatherName"));
                    v.add(rs.getString("motherName"));
                    v.add(rs.getString("gender"));
                    v.add(rs.getString("Dob"));
                    v.add(rs.getString("email"));
                    v.add(rs.getString("department"));
                    v.add(rs.getString("mobileNo"));
                }
                model.addRow(v);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

